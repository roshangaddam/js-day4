let colour=prompt("entercolour 'red', 'yellow' , 'green'");

switch(colour){
    case "red":
    console.log("STOP");
    break;

    case "yellow":
    console.log("READY TO GO");
    break;

    case "green":
    console.log("LET'S GO");
    break;

    
    default:console.log("SORRY IT IS NOT A TRAFFIC SIGNAL COLOUR. ENTER CORRECTLY");
    
}